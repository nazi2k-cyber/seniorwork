/** 시간 포맷 (시니어 친화적) */
export function formatTime(date) {
  const now = new Date();
  const d = new Date(date);
  const diff = now - d;
  const minutes = Math.floor(diff / 60000);
  const hours = Math.floor(diff / 3600000);
  const days = Math.floor(diff / 86400000);

  if (minutes < 1) return '방금 전';
  if (minutes < 60) return `${minutes}분 전`;
  if (hours < 24) return `${hours}시간 전`;
  if (days < 7) return `${days}일 전`;
  return `${d.getMonth() + 1}월 ${d.getDate()}일`;
}

/** 거리 포맷 */
export function formatDistance(meters) {
  if (meters < 1000) {
    return `${meters}m`;
  }
  return `${(meters / 1000).toFixed(1)}km`;
}

/** 급여 포맷 */
export function formatPay(amount, type = 'hourly') {
  const formatted = amount.toLocaleString('ko-KR');
  switch (type) {
    case 'hourly': return `시급 ${formatted}원`;
    case 'daily': return `일급 ${formatted}원`;
    case 'monthly': return `월급 ${formatted}원`;
    case 'per_task': return `건당 ${formatted}원`;
    default: return `${formatted}원`;
  }
}

/** 도보 시간 계산 (시니어 보행속도 기준: 3km/h) */
export function walkingTime(meters) {
  const minutes = Math.round(meters / 50); // 50m per minute for seniors
  if (minutes < 1) return '1분 이내';
  return `도보 ${minutes}분`;
}

/** Toast 알림 표시 */
export function showToast(message, duration = 2500) {
  let toast = document.querySelector('.toast');
  if (!toast) {
    toast = document.createElement('div');
    toast.className = 'toast';
    toast.setAttribute('role', 'alert');
    toast.setAttribute('aria-live', 'polite');
    document.body.appendChild(toast);
  }
  toast.textContent = message;
  toast.classList.add('toast--visible');

  // Haptic feedback
  if (navigator.vibrate) {
    navigator.vibrate(50);
  }

  setTimeout(() => toast.classList.remove('toast--visible'), duration);
}

/** HTML 이스케이프 */
export function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

/** Debounce */
export function debounce(fn, delay = 300) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), delay);
  };
}

/** 고유 ID 생성 */
export function generateId() {
  return Date.now().toString(36) + Math.random().toString(36).slice(2, 7);
}
